import java.util.*;
public class DataType {
    public static void main(String[]args){
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        char b = sc.next().charAt(0);
        double c =  (double)7/8;
        float f1 = 100.5f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(f1);
        
    }
}
